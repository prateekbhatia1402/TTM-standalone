    private void applyMode()
    {
        switch(mode)
        {
            case 'c':
                jAddButton.setEnabled(true);
                j_RAddButton.setEnabled(true);
                j_RNoField.setEditable(true);
                jRemoveButton.setEnabled(false);
                j_RRemoveButton.setEnabled(false);
                jUpdateButton.setEnabled(false);
                j_RUpdateButton.setEnabled(false);
                jTypeSelectorBox.removeAllItems();
                jTypeSelectorBox.addItem("Class");
                jTypeSelectorBox.addItem("Room");
                break;
            case 'e':
                jAddButton.setEnabled(false);
                j_RAddButton.setEnabled(false);
                jRemoveButton.setEnabled(true);
                j_RNoField.setEditable(false);
                j_RRemoveButton.setEnabled(true);
                jUpdateButton.setEnabled(true);
                j_RUpdateButton.setEnabled(true);
                jTypeSelectorBox.removeAllItems();
                if (currentType == 'c')
                    jTypeSelectorBox.addItem(currentClass.getId());
                else
                    jTypeSelectorBox.addItem(""+currentRoom.getId());
        }
    }
