    private void jAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddButtonActionPerformed
            String id = jClassIdField.getText();
        String className = jClassNameField.getText();
        if (className == null || className.isBlank() || className.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Invalid Class Name");
            return;
        }
        int roomId = jRoomBox.getSelectedIndex() > 0 ?
        Integer.valueOf(""+jRoomBox.getSelectedItem()) : -1;
        if (roomId == -1)
        {
            JOptionPane.showMessageDialog(this, "Invalid Room Selection");
            return;
        }
        int status = I_ClassControl.createClass(new I_Class(id, className, roomId));
        if (status > 0){
         JOptionPane.showMessageDialog(this, "Class Created");
         closeForm();
        }
        else
            JOptionPane.showMessageDialog(this, "Operation Failed");
    }//GEN-LAST:event_jAddButtonActionPerformed

